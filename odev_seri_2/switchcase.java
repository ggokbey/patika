package odev2;
public class switchcase {
    public static void main(String[] args) {
        int mevsim = 4;
        switch(mevsim)
        {
            case 1:
            System.out.println("Yaz Mevsimi.");
            break;

            case 2:
            System.out.println("Kış mevsimi");
            break;

            case 3:
            System.out.println("İlk Bahar mevsimi.");
            break;

            case 4:
            System.out.println("Son bahar mevsimi.");
        }
    }
    
}
